    private void btnReportDepartemenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportDepartemenActionPerformed
        try {
            // Lokasi file PDF
            String fileName = "Laporan_Departemen.pdf";

            // Membuat dokumen PDF
            Document document = new Document();
            PdfWriter.getInstance(document, new FileOutputStream(fileName));

            // Membuka dokumen
            document.open();

            // Menambahkan judul laporan
            document.add(new Paragraph("Laporan Data Departemen", FontFactory.getFont(FontFactory.HELVETICA_BOLD, 16)));
            document.add(new Paragraph(" "));

            // Membuat tabel PDF
            PdfPTable table = new PdfPTable(2); // 2 kolom
            table.setWidthPercentage(100);
            table.addCell("ID Departemen");
            table.addCell("Nama Departemen");

            // Ambil data dari tabel GUI
            DefaultTableModel model = (DefaultTableModel) tblDepartemen.getModel();
            for (int i = 0; i < model.getRowCount(); i++) {
                table.addCell(model.getValueAt(i, 0).toString());
                table.addCell(model.getValueAt(i, 1).toString());
            }

            // Tambahkan tabel ke dokumen
            document.add(table);

            // Menutup dokumen
            document.close();

            // Notifikasi sukses
            JOptionPane.showMessageDialog(this, "Laporan berhasil disimpan di file " + fileName, "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat membuat laporan!", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnReportDepartemenActionPerformed

