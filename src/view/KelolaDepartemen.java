    private void btnUbahDepartemenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahDepartemenActionPerformed
        try {
            int selectedRow = tblDepartemen.getSelectedRow();
            if (selectedRow >= 0) {
                int idDepartemen = Integer.parseInt(txtIdDepartemen.getText());
                String nama = txtNamaDepartemen.getText();

                Departemen departemen = new Departemen(idDepartemen, nama);
                departemenController.updateDepartemen(departemen);

                loadTableDepartemen();
                resetForm();

                // Notifikasi pop-up
                JOptionPane.showMessageDialog(this, "Data departemen berhasil diubah!",
                        "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat mengubah data!",
                    "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnUbahDepartemenActionPerformed

    private void btnHapusDepartemenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusDepartemenActionPerformed
        try {
            int selectedRow = tblDepartemen.getSelectedRow();
            if (selectedRow >= 0) {
                int idDepartemen = (int) tblDepartemen.getValueAt(selectedRow, 0);

                departemenController.deleteDepartemen(idDepartemen);

                loadTableDepartemen();
                resetForm();

                // Notifikasi pop-up
                JOptionPane.showMessageDialog(this, "Data departemen berhasil dihapus!",
                        "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menghapus data!",
                    "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnHapusDepartemenActionPerformed

