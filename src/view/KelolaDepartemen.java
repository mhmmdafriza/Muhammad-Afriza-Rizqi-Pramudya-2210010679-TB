    private void loadTableDepartemen() {
        DefaultTableModel model = (DefaultTableModel) tblDepartemen.getModel();
        model.setRowCount(0);

        List<Departemen> departemenList = departemenController.getAllDepartemen();
        for (Departemen departemen : departemenList) {
            Object[] row = {departemen.getIdDepartemen(), departemen.getNamaDepartemen()};
            model.addRow(row);
        }
    }

    private void fillFormFromTable() {
        int selectedRow = tblDepartemen.getSelectedRow();
        if (selectedRow >= 0) {
            // Ambil data dari tabel
            int idDepartemen = (int) tblDepartemen.getValueAt(selectedRow, 0);
            String namaDepartemen = (String) tblDepartemen.getValueAt(selectedRow, 1);

            // Isi data ke TextField
            txtIdDepartemen.setText(String.valueOf(idDepartemen));
            txtNamaDepartemen.setText(namaDepartemen);
        }
    }

    private void resetForm() {
        txtIdDepartemen.setText("");
        txtNamaDepartemen.setText("");
        tblDepartemen.clearSelection();
    }

